#pragma once
#include "system.hpp"

inline System make_solar_system() {
    constexpr double CONV = (86400.0 * 86400.0) / (149597870.7 * 149597870.7 * 149597870.7);
    constexpr double GM_SUN_KM = 132712440041.279419;
    const double G = GM_SUN_KM * CONV;

    // GM values (km^3/s^2); masses stored as mu_j / mu_sun
    const double GM_KM[] = {
        GM_SUN_KM,
        22031.868551,      // Mercury
        324858.592000,     // Venus
        398600.435507,     // Earth
        42828.375816,      // Mars
        126712764.100000,  // Jupiter
        37940584.841800,   // Saturn
        5794556.400000,    // Uranus
        6836527.100580,    // Neptune
        975.500000,        // Pluto
        62.62890,          // Ceres
        17.288245,         // Vesta
    };

    const double pos[][3] = {
        {-7.967955691533730e-03, -2.906227441573178e-03,  2.103054301547123e-04}, // Sun
        {-2.825983269538632e-01,  1.974559795958082e-01,  4.177433558063677e-02}, // Mercury
        {-7.232103701666379e-01, -7.948302026312400e-02,  4.042871428174315e-02}, // Venus
        {-1.738192017257054e-01,  9.663245550235138e-01,  1.553901854897183e-04}, // Earth
        {-3.013262392582653e-01, -1.454029331393295e+00, -2.300531433991428e-02}, // Mars
        { 3.485202469657674e+00,  3.552136904413157e+00, -9.271035442798399e-02}, // Jupiter
        { 8.988104223143450e+00, -3.719064854634689e+00, -2.931937777323593e-01}, // Saturn
        { 1.226302417897505e+01,  1.529738792480545e+01, -1.020549026883563e-01}, // Uranus
        { 2.983501460984741e+01, -1.793812957956852e+00, -6.506401132254588e-01}, // Neptune
        { 1.720200478843485e+01, -3.034155683573043e+01, -1.729127607100611e+00}, // Pluto
        {-1.103880510367569e+00, -2.533340440444230e+00,  1.220283937721780e-01}, // Ceres
        {-8.092549658731499e-02,  2.558381434460076e+00, -6.695836142398572e-02}, // Vesta
    };

    const double vel[][3] = {
        { 4.875094764261564e-06, -7.057133213976680e-06, -4.573453713094512e-08}, // Sun
        {-2.232165900189702e-02, -2.157207103176252e-02,  2.855193410495743e-04}, // Mercury
        { 2.034068201002341e-03, -2.020828626592994e-02, -3.945639843855159e-04}, // Venus
        {-1.723001232538228e-02, -2.967721342618870e-03,  6.382125383116755e-07}, // Earth
        { 1.424832259345280e-02, -1.579236181580905e-03, -3.823722796161561e-04}, // Mars
        {-5.470970658852281e-03,  5.642487338479145e-03,  9.896190602066252e-05}, // Jupiter
        { 1.822013845554067e-03,  5.143470425888054e-03, -1.617235904887937e-04}, // Saturn
        {-3.097615358317413e-03,  2.276781932345769e-03,  4.860433222241686e-05}, // Uranus
        { 1.676536611817232e-04,  3.152098732861913e-03, -6.877501095688201e-05}, // Neptune
        { 2.802810313667557e-03,  8.492056438614633e-04, -9.060790113327894e-04}, // Pluto
        { 8.978653480111301e-03, -4.873256528198994e-03, -1.807162046049230e-03}, // Ceres
        {-1.017876585480054e-02, -5.452367109338154e-04,  1.255870551153315e-03}, // Vesta
    };

    const int N = 12;
    System sys;
    sys.N = N;
    sys.G = G;
    sys.x.resize(N);
    sys.v.resize(N);
    sys.m.resize(N);

    for (int i = 0; i < N; ++i) {
        sys.m[i] = GM_KM[i] / GM_SUN_KM;
        sys.x[i] = {pos[i][0], pos[i][1], pos[i][2]};
        sys.v[i] = {vel[i][0], vel[i][1], vel[i][2]};
    }

    // Center of mass correction
    Vec3 x_cm{}, v_cm{};
    double M = 0.0;
    for (int i = 0; i < N; ++i) {
        x_cm += sys.x[i] * sys.m[i];
        v_cm += sys.v[i] * sys.m[i];
        M += sys.m[i];
    }
    x_cm = x_cm / M;
    v_cm = v_cm / M;
    for (int i = 0; i < N; ++i) {
        sys.x[i] -= x_cm;
        sys.v[i] -= v_cm;
    }

    return sys;
}
